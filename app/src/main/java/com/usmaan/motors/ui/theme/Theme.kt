val fontStyleListTitle =
    TextStyle(color = Color.DarkGray, fontSize = 18.sp, fontWeight = FontWeight.Bold)
val fontStyleListText =
    TextStyle(color = Color.DarkGray, fontSize = 14.sp, fontWeight = FontWeight.Normal)